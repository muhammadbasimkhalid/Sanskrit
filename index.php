    $(document).ready(function(){
        let modalShown = getCookie('banner');
        if (modalShown == "") {
            $.ajax({
                url: "api/announce.php",
                type: 'GET',
                dataType: 'json',
                success: function(data) {
                    let localData = data.data[0];
                    let expiryDate = new Date(localData.expiry_date);
                    let todayDate = new Date();

                    if (expiryDate > todayDate) {
                        $('#banner-body').append(localData.text);
                        $('#exampleModal').modal('toggle');
                        setCookie("banner", 5);
                    }
                    
                },
                error: function (xhr, ajaxOptions, thrownError) {
                    console.log(xhr.status);
                    console.log(xhr.responseText);
                    console.log(xhr);
                }
            });
        }
    });

    function setCookie(cname, exmin) {
        const d = new Date();
        d.setTime(d.getTime() + (exmin * 60 * 1000));
        let expires = "expires=" + d.toUTCString();
        document.cookie = cname + "=t" + ";" + expires + ";path=/";
    }

    function getCookie(cname) {
        let name = cname + "=";
        let decodedCookie = decodeURIComponent(document.cookie);
        let ca = decodedCookie.split(';');
        for (let i = 0; i < ca.length; i++) {
            let c = ca[i];
            while (c.charAt(0) == ' ') {
                c = c.substring(1);
            }
            if (c.indexOf(name) == 0) {
                return c.substring(name.length, c.length);
            }
        }
        return "";
    }
   
</script>